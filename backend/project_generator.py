def render_storage_service():
    return """import json
import os
from datetime import datetime

DATA_PATH = os.path.join(os.path.dirname(os.path.dirname(__file__)), "data", "app_data.json")


def _read():
    if not os.path.exists(DATA_PATH):
        return {"records": []}
    with open(DATA_PATH, "r", encoding="utf-8") as handle:
        return json.load(handle)


def _write(payload):
    with open(DATA_PATH, "w", encoding="utf-8") as handle:
        json.dump(payload, handle, indent=2)


def append_record(category, data):
    payload = _read()
    payload.setdefault("records", []).append(
        {
            "category": category,
            "data": data,
            "timestamp": datetime.utcnow().isoformat(),
        }
    )
    _write(payload)


def build_dashboard_stats():
    payload = _read()
    records = payload.get("records", [])
    categories = sorted({item["category"] for item in records})
    return {
        "total_submissions": len(records),
        "module_count": len(categories),
        "categories": categories,
        "last_updated": records[-1]["timestamp"] if records else "No activity yet",
    }
"""


