BASE_DIR = os.path.abspath("../generated_projects")


def create_project_structure(project_name):
    project_path = os.path.join(BASE_DIR, project_name)
    templates_path = os.path.join(project_path, "templates")
    routes_path = os.path.join(project_path, "routes")
    static_path = os.path.join(project_path, "static")
    services_path = os.path.join(project_path, "services")
    tests_path = os.path.join(project_path, "tests")
    data_path = os.path.join(project_path, "data")

    os.makedirs(project_path, exist_ok=True)
    os.makedirs(templates_path, exist_ok=True)
    os.makedirs(routes_path, exist_ok=True)
    os.makedirs(static_path, exist_ok=True)
    os.makedirs(services_path, exist_ok=True)
    os.makedirs(tests_path, exist_ok=True)
    os.makedirs(data_path, exist_ok=True)

    for package_path in (routes_path, services_path):
        init_path = os.path.join(package_path, "__init__.py")
        with open(init_path, "w") as f:
            f.write("")

    return project_path


