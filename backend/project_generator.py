def write_files(project_path, context, html_code, backend_code):
    module = context["module"]
    html_path = os.path.join(project_path, "templates", f"{module}.html")
    with open(html_path, "w", encoding="utf-8") as f:
        f.write(html_code)

    route_path = os.path.join(project_path, "routes", f"{module}.py")
    with open(route_path, "w", encoding="utf-8") as f:
        f.write(backend_code)

    app_path = os.path.join(project_path, "app.py")
    with open(app_path, "w", encoding="utf-8") as f:
        f.write(render_app_py(module))

    config_path = os.path.join(project_path, "config.py")
    with open(config_path, "w", encoding="utf-8") as f:
        f.write(render_config())

    base_template_path = os.path.join(project_path, "templates", "base.html")
    with open(base_template_path, "w", encoding="utf-8") as f:
        f.write(render_base_template(context["title"]))

    home_template_path = os.path.join(project_path, "templates", "home.html")
    with open(home_template_path, "w", encoding="utf-8") as f:
        f.write(render_home_template(module, context["title"]))

    success_template_path = os.path.join(project_path, "templates", "success.html")
    with open(success_template_path, "w", encoding="utf-8") as f:
        f.write(render_success_template())

    dashboard_template_path = os.path.join(project_path, "templates", "dashboard.html")
    with open(dashboard_template_path, "w", encoding="utf-8") as f:
        f.write(render_dashboard_template())

    css_path = os.path.join(project_path, "static", "style.css")
    with open(css_path, "w", encoding="utf-8") as f:
        f.write(render_style())

    storage_path = os.path.join(project_path, "services", "storage.py")
    with open(storage_path, "w", encoding="utf-8") as f:
        f.write(render_storage_service())

    requirements_path = os.path.join(project_path, "requirements.txt")
    with open(requirements_path, "w", encoding="utf-8") as f:
        f.write("flask\n")

    readme_path = os.path.join(project_path, "README.md")
    with open(readme_path, "w", encoding="utf-8") as f:
        f.write(render_readme(context))

    setup_path = os.path.join(project_path, "setup.bat")
    with open(setup_path, "w", encoding="utf-8") as f:
        f.write(render_setup_bat())

    env_path = os.path.join(project_path, ".env.example")
    with open(env_path, "w", encoding="utf-8") as f:
        f.write(render_env_example())

    quickstart_path = os.path.join(project_path, "QUICKSTART.txt")
    with open(quickstart_path, "w", encoding="utf-8") as f:
        f.write(render_quickstart())

    tests_path = os.path.join(project_path, "tests", "test_app.py")
    with open(tests_path, "w", encoding="utf-8") as f:
        f.write(render_tests(module))

    data_path = os.path.join(project_path, "data", "app_data.json")
    with open(data_path, "w", encoding="utf-8") as f:
        json.dump({"records": []}, f, indent=2)

    return project_path


def generate_project(context, html_code, backend_code):
    project_path = create_project_structure(context["project_name"])
    write_files(project_path, context, html_code, backend_code)
    return project_path
